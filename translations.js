// Default language
let currentLanguage = 'en';

// Function to change the language
function changeLanguage(lang) {
    currentLanguage = lang;
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
    updateContent();
    updateStyles();
}

// Function to update all text content
function updateContent() {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[currentLanguage][key]) {
            element.textContent = translations[currentLanguage][key];
        }
    });
    
    // Update placeholders for form inputs
    const inputs = document.querySelectorAll('[data-i18n-placeholder]');
    inputs.forEach(input => {
        const key = input.getAttribute('data-i18n-placeholder');
        if (translations[currentLanguage][key]) {
            input.placeholder = translations[currentLanguage][key];
        }
    });
}

// Function to update styles for RTL/LTR
function updateStyles() {
    const body = document.body;
    
    if (currentLanguage === 'ar') {
        // RTL specific styles
        body.classList.add('rtl');
        // Add any additional RTL specific styles here
    } else {
        // LTR specific styles
        body.classList.remove('rtl');
        // Remove any RTL specific styles here
    }
}

// Initialize language switcher when DOM is loaded
document.addEventListener('DOMContentLoaded', function() {
    // Create language switcher if it doesn't exist
    if (!document.getElementById('language-switcher')) {
        createLanguageSwitcher();
    }
    
    // Apply initial language
    updateContent();
});

// Function to create language switcher
function createLanguageSwitcher() {
    const navbarContainer = document.querySelector('nav .container');
    
    // Create language switcher container
    const languageSwitcher = document.createElement('div');
    languageSwitcher.id = 'language-switcher';
    languageSwitcher.className = 'flex items-center ml-4';
    
    // Create English button
    const enButton = document.createElement('button');
    enButton.className = 'px-2 py-1 text-white hover:text-yellow-400 transition duration-300 font-medium';
    enButton.textContent = 'EN';
    enButton.onclick = function() { changeLanguage('en'); };
    
    // Create separator
    const separator = document.createElement('span');
    separator.className = 'text-white mx-1';
    separator.textContent = '|';
    
    // Create Arabic button
    const arButton = document.createElement('button');
    arButton.className = 'px-2 py-1 text-white hover:text-yellow-400 transition duration-300 font-medium';
    arButton.textContent = 'عربي';
    arButton.onclick = function() { changeLanguage('ar'); };
    
    // Append buttons to language switcher
    languageSwitcher.appendChild(enButton);
    languageSwitcher.appendChild(separator);
    languageSwitcher.appendChild(arButton);
    
    // Insert language switcher before the mobile menu button
    const mobileMenuButton = document.getElementById('mobile-menu-button');
    navbarContainer.insertBefore(languageSwitcher, mobileMenuButton);
    
    // Add language switcher to mobile menu
    const mobileMenu = document.getElementById('mobile-menu');
    const mobileLanguageSwitcher = document.createElement('div');
    mobileLanguageSwitcher.className = 'flex justify-center mt-4 pt-4 border-t border-gray-100';
    
    const mobileEnButton = document.createElement('button');
    mobileEnButton.className = 'px-4 py-2 text-gray-800 hover:text-blue-600 transition duration-300 font-medium';
    mobileEnButton.textContent = 'English';
    mobileEnButton.onclick = function() { changeLanguage('en'); };
    
    const mobileSeparator = document.createElement('span');
    mobileSeparator.className = 'text-gray-300 mx-2 self-center';
    mobileSeparator.textContent = '|';
    
    const mobileArButton = document.createElement('button');
    mobileArButton.className = 'px-4 py-2 text-gray-800 hover:text-blue-600 transition duration-300 font-medium';
    mobileArButton.textContent = 'العربية';
    mobileArButton.onclick = function() { changeLanguage('ar'); };
    
    mobileLanguageSwitcher.appendChild(mobileEnButton);
    mobileLanguageSwitcher.appendChild(mobileSeparator);
    mobileLanguageSwitcher.appendChild(mobileArButton);
    
    mobileMenu.appendChild(mobileLanguageSwitcher);
}